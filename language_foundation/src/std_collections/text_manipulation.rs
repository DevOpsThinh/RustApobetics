/// A type alias for the response returned by command processing functions.
///
/// `Response` is defined as a `String` and is used to represent
/// the output or message generated by the application in response to user input.
///
/// # Example
///
/// ```
/// let resp: Response = String::from("Hello, Rustacean!");
/// ```
type Response = String;

/// Processes a user command passed as a command-line argument and logs the result.
///
/// # Panics
///
/// If no command-line argument is provided (expects a username or command).
///
/// # Example
/// ```
/// // Run the program with a command-line argument:
/// // cargo run -- <username>
/// ```
pub fn simple_cmd_program() {
    let message = std::env::args()
        .nth(1)
        .expect("Enter your username. Usage: language_foundation <message>");

    let info = process_command(&message);

    println!("{}", info);
}

/// The function constructs a greeting message by prepending "Hi, " and appending " Rustacean!"
/// to the provided command string, then returns it as a `Response`.
///
/// # Arguments
///
/// * `cmd` - A string slice representing the command or username to include in the greeting.
///
/// # Returns
///
/// * `Response` - The constructed greeting message.
///
/// # Example
///
/// ```
/// let resp = process_command("Ferris");
/// assert_eq!(resp, Response::from("\nHi, <Ferris> Rustacean!\n"));
/// ```
fn process_command(cmd: &str) -> Response {
    let mut response = Response::from("\nHi, ");
    response.push_str(cmd);

    let mut response = response.replacen(cmd, &format!("<{}>", cmd), 1);
    response.push_str(" Rustacean!");
    response.push('\n');
    response
}

